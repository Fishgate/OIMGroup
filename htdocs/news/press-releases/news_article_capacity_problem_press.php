                            <header class="news-head">Integrated, customer-orientated focus needed to improve efficiency in financial services sector</header>
                            <!--<img class="response-img" src="http://www.placehold.it/500x250" alt="News article" />-->
                            <p>The South African financial sector is facing an efficiency problem, often caused by outdated processes and technology, ineffective workforce management and segmented client service offerings. This means that administrative output is not functioning as optimally as it could, which impacts on a business’s overall performance.</p>
                            <p>This is the view of Arjen de Bruin, MD of Operation Solutions at OIM, a leading business performance specialist in South Africa. De Bruin says that administrative divisions of companies and their client service divisions (such as call centres) are using outdated processes and technologies, which is increasingly affecting production efficiency in the local financial services sector.</p>
                            <p>“Production efficiency, that is, the turnaround time of the task, is negatively affected when there are technological constraints at play. Often, in call centres for example, we see a multitude of either old technologies not compatible with more modern technology, or a variety of software technology that is not integrated or streamlined into a more effective model.</p>
                            <p>He says that financial services organisations work with a diverse array of specialty suppliers (e.g. brokers, rating agencies, financial bureaus etc.) and that these relationships constitute a complex supply chain.</p>
                            <p>“With a variety of different service providers and products, contact centre agents often work with a multitude of software systems. We are finding that call centre employees in financial institutions often find it tricky to tackle the muddle of different systems which can affect the quality of their work,” he says.</p>
                            <p>De Bruin says that with repeat calls from customers, some of the call centres send work to the back offices, which duplicates structures, making such measures ineffective and unnecessary wheels in the supply chain.</p>
                            <p>“The key to ensuring that technology improves a job is to ensure that all new systems integrate well with, or replace, older IT structures.  This not only makes it easier for the person using the system, but also improves efficiency and customer experience.”</p>
                            <p>De Bruin argues that although South Africa has the second most stable financial system in the world, and highly evolved regulatory structures, client service systems in the financial sector are not integrated, which often causes the client frustration and results in poor client service delivery.</p>
                            <p>“A person that has a variety of policies and benefits at one bank, and more policies with a subsidiary of that bank, for example, will feel frustrated at not having an integrated customer experience.  Segmentation, often in the financial services sector, causes many headaches for customers.</p>
                            <p>“Organisations should focus on applicable, customer-focused metrics, and look at optimising or integrating systems, which will lead to better customer engagement.”</p>
                            <p>Recently the University of Johannesburg released the results from its 2012 Supply Chain Skills Gap Survey, saying that there is a severe absence of supply chain skills in South Africa.  The survey said that employers indicated that although operational positions were relatively easy to fill, about 65% said it was difficult to fill tactical level positions.</p>
                            <p>In line with these findings, he says that operational management in call centres are competent, but are overloaded and therefore often ineffective in dealing with the workforce.</p>
                            <p>“We find that supervisors, who are  responsible for managing all processes, including removing obstacles and assessing overall workforce productivity, often spend most of their time trying to fill the gap created by a skills deficit in their  team. Supervisors at call centres need to possess efficient problem solving techniques, plan correctly and be able to communicate daily tasks effectively to the team, as these skills are critical to ensuring the team’s throughput.”</p>
                            <p>He said that many companies have not correctly identified the key competencies needed for supervisors to excel in this crucial position.  “The first step to increase operational efficiency is to correctly identify the key competences to make supervisors more effective and supported. For example, implementing better systems that will help the supervisor make better decisions, or getting a handle on absenteeism, may aid the supervisor.”</p>
                            <p>The Supply Chain Skills Gap Survey also found that companies are recognising the role that 'seeing the big picture' plays in driving greater productivity and effectiveness across the supply chain. De Bruin agrees that engaging with the workforce with knowledge on business acumen, as well as the company’s strategic direction will aid not only supervisors, but also their teams in understanding their purpose at the company.</p>
                            <p>“Communication with the workforce also plays a major role. We often find that many people don’t know what is expected of them on a shift; they don’t know the company’s strategy or direction, and can’t interpret the meaning of the vision and mission statements for their role in the company.</p> 
                            <p>“Communicating the employee’s purpose and direction and engaging with the workforce will result in increased throughput at the administrative backend of financial institutions and call centres,” he concludes.</p>
