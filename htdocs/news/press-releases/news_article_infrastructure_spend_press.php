                            <header class="news-head">Government's planned R827 billion infrastructure spend: But do we have the skills?</header>
                            <!--<img class="response-img" src="http://www.placehold.it/500x250" alt="News article" />-->
                            
                            <p>In his 2013 budget address, Minister of Finance, Pravin Gordhan, unveiled plans to finance R827 billion worth of infrastructure projects over the next three years - a significant increase from the R642 billion spent over the last three years. However, if Government is to reach these targets, the issue of upskilling junior and middle management in their roles needs to be adequately addressed, as they are vital cogs in South Africa’s ‘wheel of productivity.’</p>
                            <p>This is the view of Arjen de Bruin, MD of Operation Solutions at OIM International, a business consultancy firm in South Africa.</p>
                            <p>“In terms of productivity, the biggest impact on the local workforce is the first line manager, or supervisor, but the country’s supervisory skills still remain very weak.  If South Africa is to reach its new infrastructure target, substantial investment has to be made in equipping supervisors with the right skills to ensure productivity in the workforce,” he says. </p>
                            <p>Gordhan announced that R400 billion from state-owned enterprises will fund the on-going building of power stations, new electricity transmission lines, new railway lines, ports, pipelines, water-transfer schemes and fund various airport upgrades. He said that private-sector projects are also contained in the Government’s Strategic Infrastructure Projects, bringing the total value of projects to over R4 trillion.</p>
                            <p>“Although Government has committed sufficient infrastructure expenditure, our inability to execute projects in the final stage has demonstrated a problem that has to do with the climate and culture of the job, and the lack of skills in the arena. Slow progress with capital projects is responsible for major frustration in the country, which is apparent with the large number of strikes held in the sector,” says Tjaart Minnaar, MD of OIM International.</p>
                            <p>Minnaar refers to the Medupi power plant example, which is set to be Eskom’s fourth-biggest project and is scheduled to start producing electricity at the end of the year.” However, contractors have asked for more time to complete the project, as various problems have been encountered since the start of construction, mostly relating to the high technical nature of the job. In addition, Eskom has been battling with strikes and work delivery at the plant.”</p>
                            <p>A recent report to Parliament stated that service delivery by the public sector is fundamentally handicapped by a shortage of planners and engineers with the ability to prepare suitable concept specifications and tenders for projects, as well as by poor capital expenditure planning and poorly managed procurement channel processes.</p>
                            <p>What is needed, then, to better equip planners, engineers and supervisors with the ability to execute their jobs?</p>
                            <p>Minnaar explains that by ensuring that the right corporate culture and climate is in place is the first step to creating an environment that enables successful completion of projects. “Companies have to ensure a just and equitable workplace environment, which includes having policies, practices and procedures in place that are legally correct and suited to the company’s culture, such as disciplinary codes and employment equity practices. These must be consistently applied to create the right climate and culture in the workplace.</p>
                            <p>“It is also essential that leaders, managers and supervisors be properly trained and equipped to contribute to a positive, productive and stable workplace.  Staff needs to understand where the company is heading in terms of vision and strategy, and they must know how this translates into goals and targets that apply to them.”   </p>
                            <p>De Bruin echoes Minnaar’s point, as he argues that the biggest impact on a workforce is the supervisor, as he or she is responsible for managing all processes, removing obstacles and assessing overall workforce productivity. “Supervisors therefore need to possess efficient problem solving techniques, plan correctly and be able to communicate daily tasks effectively to the team, as these skills are critical for ensuring workforce throughput.</p>
                            <p>“In fact, 80% of the supervisor’s time should be spent ensuring the team is maximising production output each day, rather than spending most of his time trying to fill the gap created by a skills deficit in his team.”</p>
                            <p>De Bruin says that, if a supervisor is trained correctly in fulfilling his role, companies are able to improve productivity levels by an average of 10% to 15%. In a recent successful pilot project conducted by OIM International on a South African mine that focused on optimising labour productivity by empowering supervisors, the competencies of the supervisors increased by an average of 10%, while planning and execution competencies increased by 30%. As a result production output improved between 15% and 25% over the four months.</p>
                            <p>“These results show a direct link between the intervention’s effectiveness and increased output. The results highlight the importance of the training, as it not only impacts the supervisors by making them more efficient, but also empowers them to manage their  teams more effectively and increase the workforce productivity in the long-term,” concludes de Bruin.</p>
