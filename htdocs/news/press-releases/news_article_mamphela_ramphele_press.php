                            <header class="news-head">DR. Mamphela Ramphele's shared value in business: Is it possible in SA?</header>
                            <!--<img class="response-img" src="http://www.placehold.it/500x250" alt="News article" />-->
                            
                            <em><b>By <span class="author">Tjaart Minnaar</span>, Managing Director of OIM International, one of South Africa’s leading business consultancy firms</b></em>

                            <p>We are at a defining moment in our country’s history. The tragic violence brought on by Marikana, the multitude of service boycotts, countless reports of corruption, ineffective policy execution and high inequality levels in South Africa, are indicators that the country has broadly-based value creation issues.</p>
                            <p>A complete change in stakeholder thought and action is required at a national, industry and company level to create shared value for all.</p>
                            <p>I echo Dr. Mamphela Ramphele’s recent call to build on sustainable, local, economic development programmes. However, although possible, shared value will require a complete ideology shift whereby shareholders, customers, suppliers, employees and society at large will benefit.</p>
                            <p>The central premise behind creating shared value is that the competitiveness of a country, industry or company and the wellbeing of the communities around it are mutually dependent.</p>
                            <p>Positively utilising the relationship between our society and economic progress has the potential to redefine our idea of modern day capitalism. Shared value is the notion that value principles and benefits relative to cost should be applied to businesses and organisations, in order to achieve economic and social progress, and uplift society.</p>
                            <p>A business that benefits its surrounding community, but utilises a model intent on benefiting all stakeholders, understands the concept of shared value.  The concept is not new, as many large businesses like Barloworld, PPC Cement, Unilever and other corporates have been on that path for the past 10 years.</p>
                            <p>The concept of shared value was first coined by Michael Porter in the Harvard Business Review. The article elaborates on relevant cases where companies have developed deep and meaningful business strategies relative to their Corporate Social Investment initiatives. </p>
                            <p>Mining magnate Patrice Motsepe, South Africa’s wealthies black man, recently made history by announcing the donation of half his wealth to the Motsepe Foundation.  In response to this action, Professor Arnold Smit, Director of the USB-ED Centre for Business in Society at the University of Stellenbosch Business School, called the decision not only historic, but a sign that wealth created through business should benefit not just owners and shareholders, but also other stakeholders in society. I agree that this decision came from a deeper inner conviction and it was done in consideration of the interests of shareholders and investors.</p> 

                            <p>This announcement also came at a time when South Africa was revealed as one of the most unequal societies in the world by Oxfam, while the World Economic Forum highlighted inequality as one of the top global risks of 2013. The Oxfam report suggested that, with a Gini coefficient of 0,63 in 2011,  inequality in South Africa is increasing. A Gini coefficient of zero means a country has complete equality.</p>

                            <p>Digging deeper, the roots of these issues run into a society where the inequality between the haves and the haves-not is very large, and our traditional, biggest revenue spinner, our natural resources, is slowly running out.  In order to move forward as a country, a huge mindset change is required for business leaders to work together in adopting ideals true the shared value approach.</p>
                            <p>The work of the National Planning Commission (NPC) can go a long way in developing a unifying vision for the country, and provide reason for stakeholders to work together. The NPC is responsible for developing a long-term strategic plan to uplift the country. However, this can only be executed under exceptional leadership.</p>
                            <p>In South Africa with its unique history, the government should take the leadership to create shared value thinking, especially to get stakeholders and role players to collaborate and engage about what needs to change.</p>
                            <p>Yet, South Africans have become increasingly disillusioned by the self-gain shown by people in power, both in the private and public sectors. A change in political authority has not fundamentally addressed the structural fault lines in our society. Now more than ever, the country is in need of strong and decisive leaders who can implement the concept of shared value; someone with broad-ranging credibility who is practised at understanding business processes, candidates such as Cyril Ramaphosa.</p>
                            <p>Aside from choosing the right candidate(s), effective goal-orientated discussion is still required, as well as action that needs to achieve by means other than through ‘talk shops’. Skill sets and capabilities of different stakeholders need to be fully utilised, in order to create a map of effective shared value approaches.</p>
                            <p>This will have to be followed by similar stakeholder forums at a civic level to focus on community value creation, involving all stakeholders in the value chain.  At the same time, however, nothing stops South African businesses to lead the change that is required by adopting their own approach, vision and model to shared value creation. As the verse says, ‘where there is no vision, the people perish.’</p>
