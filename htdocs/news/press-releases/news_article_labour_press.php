                            <header class="news-head">The future of Labour Relations in SA</header>
                            <!--<img class="response-img" src="http://www.placehold.it/500x250" alt="News article" />-->
                            <p>02 August 2013: South Africa’s labour relations is at a defining moment in our country’s history. Over the past few years, unprotected strike action has escalated into an uncontrolled, violent and unlawful landscape, led by a mob mentality in the absence of formal and recognised leaders.</p>

<p>This is the view of Chris Jacobs, conflict resolution expert at OIM, one of South Africa’s leading business performance specialists. “On the one hand, this is the result of major social frustration. Unfortunately, it is the workplace that has become the warzone, with grave consequences for employers. On the other hand, it is also a result of complacency which has set in with and between businesses and labour leaders, as well as legislation which has not adapted to the changing circumstances.”</p>

<p>Jacobs says there is no doubt that the labour relations landscape in South Africa needs to change to an environment of normality, stability and order. “In order for this to happen, employers need to re-establish constructive relationships with all employees, not only union leaders, and manage both legislative challenges as well as economic demands. To achieve this, it is imperative that sound employee relations strategies be developed rather than short-term re-active tactics.”</p>

<p>Jacobs argues that change needs to happen at a union, employee and employer level.</p>

<p>Unions face serious challenges right now, with a recent Human Sciences Research Council (HSRC) study revealing that South Africans’ trust in trade unions dropped sharply from 43% in 2011 to 29% in 2012. According to Jacobs, this trend is in line with developed economies.</p>

<p>He says some of the challenges faced by unions include diminishing membership numbers, a disconnection with their members and unhealthy competition with each other. “This has lead not only to the destabilisation of the workplace but also intimidation, injury and loss of lives. Even though this is nothing new, it now happens even more so in organisations within the same “broad circles/domain”.</p>

<p>“In the Congress of South African Trade Unions (Cosatu), affiliates such as NUM and NUMSA, FAWU and SACCAWU to name a few, are fighting it out for the hearts and minds of members in the same workplace. The policy of one country–one federation–one industry–one union has been allowed to be trashed by affiliates. If affiliates do not even respect the policies of its mother organisation, how will they respect the policies of companies in which their members operate. The workplace has become a battleground again like in the 1980s, to the detriment of the employer. Unions need to seriously re-establish contact with their members, and take responsibility for their own actions and those of their members.”</p>

<p>According to Jacobs, centralised bargaining is another labour practise that is currently experiencing a crisis. He says that this is a reality that needs to be faced and appropriate strategies needs to be followed.</p>

<p>“Centralised bargaining was traditionally favoured by the bigger unions that were in favour of the concept of majoritarianism - the union that forms the majority has all the organisational and bargaining rights. In the process, such unions became the sole voice for all employees. This defeated the objective of workplace democracy in essence. In cases where this situation has now been turned around, some unions prefer to go back to decentralised bargaining as it puts them in a position to deal with site specific issues and gives them a voice which they would not have had in a winner-takes-it-all situation of centralised bargaining.”</p>

<p>Jacobs explains that site-specific bargaining is more demanding of employers, as they often have to invest the same effort into each discussion, but is more rewarding in the long term as potential conflict situations can be avoided. “We’ve often seen that in many cases a relationship is built with a union and an employer with a centralised bargaining approach, but there is no recognition of other unions playing a role in the workplace. This creates tension between employees, unions and the employer.”</p>

<p>Despite all the challenges, Jacobs is optimistic about the possibility of positive change in the current labour relations landscape.</p>

<p>“Change is possible, but one has to realise that the wheels turn slowly.  For example, the 1995 Labour Relations Act (LRA) was designed for the circumstances of that period, which means that majoritarianism which may have been appropriate then, is largely out-dated now, especially as workplace democracy is not being upheld. This needs to be revisited. Currently we have a whole chapter in the LRA, chapter four on Workplace Forums, which is rarely applied if at all, save for two or three definitions. No workplace forums, as intended by this chapter, currently exist.”</p>

<p>He says employers and unions cannot wait for legislative changes before taking action. “Companies must ensure that they have a sound Employee Relations (ER) and Human Resources (HR) strategy in place and act proactively, not reactively when conflict situations arise. This means that the current mindset of business leaders has to change; they can no longer see ER as the ‘soft side’ of the business. Last year showed that the absence or lack of a pro-active strategy can cost millions and cause the loss of lives.”</p>

<p>Jacobs explains that the first step in creating an ER strategy is to ensure a fair workplace climate and the right corporate culture.</p>

<p>“Companies have to ensure an equitable workplace, which includes having policies, practices and procedures in place that are legally correct and suited to the company’s culture. This can include disciplinary codes and employment equity practices, and must be consistently applied to create the right climate and culture in the workplace. Furthermore, appointed leaders have to guide the way and create a climate beneficial to peace and harmony in the workplace.</p>

<p>“Staff need to understand where the company is heading in terms of vision and strategy and they must know how this translates into goals that apply to them. This includes discussing the company strategy and organisational values, analysing team goals and targets, participating in collective risk identification and problem solving, sharing and debating company information, as well as focusing on relevant strategic and learning topics,” Jacobs argues.</p> 

<p>He suggests that management must actively take up the role of primary communicator. “Open and honest communication directly with the workforce, rather than relying on unions, will allow correct and consistent messages to be communicated. It will also avoid the problem of not reaching staff with information in cases where staff members are dissatisfied with, or reject union leaders, and thus also their messages. For these purposes structures need to be established throughout the organisation to communicate the right messages. Moreover, leaders need to be trained to communicate effectively and to deal with issues before they get out of hand.”</p>

<p>Jacobs says that the aim is to foster holistic business understanding among the workforce, through training on global, political and economic realities, labour rights and responsibilities, sector-specific developments, new legislation, productivity and people management.</p>

<p>“Lastly, employee relations practitioners, shop stewards and leaders in general need to be up-skilled in industrial relations and conflict resolution through interactive workshops. This will equip them to interpret trends and to identify, understand and deal with potential conflict situations before they happen,” concludes Jacobs.</p>
